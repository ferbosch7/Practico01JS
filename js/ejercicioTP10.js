let numero = parseInt(prompt("ingrese un numero"));


if(numero % 2 === 0){
    alert(numero + " es divisible por 2");
} else if(numero % 3 === 0){
    alert(numero + " es divisible por 3");
} else if(numero % 5 === 0){
    alert(numero + " es divisible por 5");
} else if(numero % 7 === 0){
    alert(numero + " es divisible por 7");
}